function sumOfFirstFivePrimes() {
    let primes = []; // Масив для зберігання простих чисел
    let num = 2; // Починаємо перевірку з числа 1
    let sum = 0; // Змінна для зберігання суми перших п'яти простих чисел
    let primeCount = 0; // Лічильник знайдених простих чисел

    while (primeCount < 5) { // Перевіряємо, чи ми знайшли п'ять простих чисел
        let isPrime = true; // Прапорець, що показує, чи є поточне число простим

        // Перевіряємо, чи є поточне число простим
        for (let i = 2; i <= Math.sqrt(num); i++) {
            if (num % i === 0) {
                isPrime = false;
                break;
            }
        }

        // Якщо число є простим, додаємо його до масиву простих чисел
        if (isPrime) {
            primes.push(num);
            sum += num; // Додаємо число до суми перших п'яти простих чисел
            primeCount++; // Збільшуємо лічильник знайдених простих чисел
        }

        num++; // Переходимо до наступного числа
    }

    return sum; // Повертаємо суму перших п'яти простих чисел
}

// Виклик функції та виведення результату
console.log(sumOfFirstFivePrimes()); // Повинно вивести суму перших п'яти простих чисел
